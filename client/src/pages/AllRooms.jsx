import React, { useMemo, useState } from 'react'
import {assets, facilityIcons, roomsDummyData} from '../assets/assets'
import {useNavigate, useSearchParams} from 'react-router-dom'
import StarRating from '../components/StarRating'
import { useAppContext } from '../context/AppContext'

const CheckBox = ({ label, selected = false, onChange = () => {} }) => {
  return (
    <label className="flex items-center cursor-pointer mt-2 text-sm select-none">
      <input 
        type="checkbox" 
        checked={selected} 
        onChange={(e) => onChange(e.target.checked, label)}
        className="w-5 h-5 text-blue-600 border-gray-300 rounded focus:ring-2 focus:ring-blue-300 transition duration-150 shrink-0"
      />
      <span className="ml-3 leading-5 text-gray-700">{label}</span>
    </label>
  );
}

const RadioButton = ({ label, selected = false, onChange = () => {} }) => {
  return (
    <label className="flex items-center cursor-pointer mt-2 text-sm select-none">
      <input 
        type="radio" 
        name="sortOption" 
        checked={selected} 
        onChange={() => onChange(label)}
        className="w-5 h-5 text-blue-600 border-gray-300 rounded-full focus:ring-2 focus:ring-blue-300 transition duration-150 shrink-0"
      />
      <span className="ml-3 leading-5 text-gray-700">{label}</span>
    </label>
  );
}



const AllRooms = () => {
  const [searchParams, setSearchParams] = useSearchParams();
  const {rooms, navigate, currency} = useAppContext();
  const [openFilters, setopenFilters] = useState(false);
  const [selectedFilters, setSelectedFilters] = useState({
    roomTypes: [],
    priceRanges: [],
  });
  const [selectedSort, setSelectedSort] = useState('')

  const roomTypes = [
      "single bed",
      "double bed",
      "luxury room",
      "family suite",
  ];
  const priceRanges = [
    '0 to 500',
    '500 to 1000',
    '1000 to 2000',
    '2000 to 3000',
  ]

  const sortOptions = [
    'Price Low to Hight',
    'Price Hight to Low',
    'Newest First'
  ]

  // handle changes for filters and sorting
  const handleFilterChange = (checked, value, type) =>{
    setSelectedFilters((prevFilters)=>{
      const updatedFilters = {...prevFilters};
      if(checked){
        updatedFilters[type].push(value);
      }else{
        updatedFilters[type] = updatedFilters[type].filter(item => item !== value);
      }
      return updatedFilters;
    })
  }

const handleSortChange = (sortOption)=>{
  setSelectedSort(sortOption);
}

//fuction to check if a room matches the selected room types
const matchesRoomType = (room)=>{
  return selectedFilters.roomTypes.length === 0 || selectedFilters.roomTypes.includes(room.roomTypes)
}

// function to check if a room matches the selected price ranges
const matchesPriceRange = (room)=>{
  return selectedFilters.priceRanges.length === 0 || selectedFilters.priceRanges.some(range => {
    const [min, max] = range.split(' to ').map(Number);
    return room.pricePerNight >= min && room.pricePerNight <= max;
  })
}
// function to sort rooms based on the selected sort option
const sortRooms = (a, b) =>{
  if (selectedSort === 'Giá từ thấp lên cao'){
    return a.pricePerNight - b.pricePerNight
  }
  if (selectedSort === 'Giá từ cao xuống thấp'){
    return b.pricePerNight - a.pricePerNight
  }
  if (selectedSort === 'Mới nhất'){
    return new Date(b.createdAt) - new Date(a.createdAt)
  }
  return 0;
}
  // filter destination
  const filterDestination = (room) => {
    const destination = searchParams.get('Đích đến');
    if(!destination) return true;
    return room.hotel.city.toLowerCase().includes(destination.toLowerCase())
  }
  // filter and sort rooms based on the selected filters and sort option
  const filteredRooms = useMemo(()=>{
    return room.filter(room => matchesRoomType(room) && matchesPriceRange(room) && filterDestination(room)).sort(sortRooms);

  },[rooms, selectedFilters, selectedSort, searchParams])

  //clear filter 
  const clearFilters = () => {
    setSelectedFilters({
      roomTypes: [],
      priceRanges: [],
    });
    setSelectedSort('');
    searchParams({});
  }


  return (
    <div className='flex flex-col-reverse lg:flex-row items-start justify-between pt-28 gap-20  md:pt-35 px-4 md:px-16 lg:px-24 xl:px-32'>
        <div>
          <div className='flex flex-col items-start text-left'>
            <h1 className='font-playfair text-4xl md:text-[40px]'>Phòng Khách Sạn</h1>
            <p className='text-sm md:text-base text-gray-500/90 mt-2 max-w-174 '>Hãy tận dụng các ưu đãi có thời hạn và gói đặc biệt của chúng tôi để nâng cao trải nghiệm lưu trú và tạo nên những kỷ niệm khó quên.</p>
          </div>

          {filteredRooms.map((room)=>(
            <div key={room._id} className='flex flex-col md:flex-row items-start py-10 gap-6 border-b border-gray-300 last:pb-30 last:border-0'>
              <img onClick={()=> {navigate(`/rooms/${room._id}`); scrollTo(0,0)}}
               src={room.images[0]} alt="hotel-img" title='View Room Details' className='max-h-56 md:w-1/2 rounded-xl shadow-lg object-cover cursor-pointer'/>
               <div className='md:w-1/2 flex flex-col gap-2'>
                <p className='text-gray-500'>{room.hotel.city}</p>
                <p onClick={()=> {navigate(`/rooms/${room._id}`); scrollTo(0,0)}} className='text-gray-800 text-3xl font-playfair cursor-pointer'>{room.hotel.name}</p>
                <div className='flex items-center'>
                  <StarRating/>
                  <p className='ml-2'>Hơn 200 lượt đánh giá</p>
                </div>
                <div className='flex items-center gap-1 text-gray-500 mt-2 text-sm'>
                  <img src={assets.locationIcon} alt="location-icon" />
                  <span>{room.hotel.address}</span>
                </div>
                {/* Room Amenties */}
                <div className='flex flex-wrap items-center mt-3 mb-6 gap-4'>
                    {room.amenities.map((item, index)=>(
                      <div key={index} className='flex-items-center gap-2 px-3 py-2 rounded-lg bg-[#F5F5FF]/70'>
                        <img src={facilityIcons[item]} alt={item} className='w-5 h-5'/>
                        <p className='text-xs'>{item}</p>
                      </div>
                    ))}
                </div>
                {/* Room Price per Night */}
                <p className='text-xl font-medium text-gray-700'>${room.pricePerNight} /đêm</p>
               </div>
            </div>
          ))}
          
        </div>
        {/* Filters */}
        <div className='bg-white w-80 border border-gray-300 text-gray-600 max-lg:mb-8 lg:mt-16'>

          <div className={`flex items-center justify-between px-5 py-2.5 lg:border-b border-gray-300 ${openFilters && "border-b"}`}>
            <p className='text-base font-medium text-gray-800'>FILTERS</p>
            <div className='text-xs cursor-pointer'>
              <span onClick={()=> setopenFilters(!openFilters)} className='lg:hidden'>
                {openFilters ? 'HIDE' : 'SHOW'}</span>
              <span className='hidden lg:block'>CLEAR</span>
            </div>
          </div>

          <div className={`${openFilters ? 'h-auto' : "h-0 lg:h-auto"} overflow-hidden`}>
            <div className='px-5 pt-5'>
              <p className='font-medium text-gray-800 pb-2'>Popular filters</p>
              {roomTypes.map((room,index)=>(
                <CheckBox key={index} label={room} selected={selectedFilters.roomTypes.includes(room)} onChange={(checked)=>handleFilterChange(checked, room, 'roomTypes')}/>
              ))}
            </div>
            <div className='px-5 pt-5'>
              <p className='font-medium text-gray-800 pb-2'>Price Range</p>
              {priceRanges.map((range,index)=>(
                <CheckBox key={index} label={`${currency} ${range}`} selected={selectedFilters.priceRanges.includes(range)} onChange={(checked)=>handleFilterChange(checked, range, 'priceRanges')}/>
              ))}
            </div>
            <div className='px-5 pt-5 pb-7'>
              <p className='font-medium text-gray-800 pb-2'>Sort By</p>
              {sortOptions.map((option,index)=>(
                <RadioButton key={index} label={option} selected= {selectedSort === option} onChange={()=> handleSortChange(option)}/>
              ))}
            </div>
          </div>

        </div>
    </div>
  )
}

export default AllRooms